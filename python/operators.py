a = 24
b = 16

#comparison
print(a==b) #equals
print(a>b) #greater than
print(a<b) #less than
print(a!=b) #not equals
print(a>=b) #greater than or equal
print(a<=b) #less than or equal

#operators
print(a+b) #addition
print(a-b) #subtraction
print(a*b) #multiplication
print(a/b) #division
print(a**b) #exponentiation
print(a//b) #floor division
print(a%b) #modulus